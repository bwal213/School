public class Int{

	private int x;
	
	public Int(int x) {
		
		this.x = x;
		
	}
	
	public void setInt(int x) {
		
		this.x = x;
		
	}
	
	public int getInt() {
		
		return this.x;
	}
	
	public int toInt() {
		return (int) this.x;
	}
}